package com.springstudy.talentshow;

public class Audience {
	public Audience() {}
	
	public void takeSeats() {
		System.out.println("[Audience] take seats.");
	}
	
	public void turnOffCellPhones() {
		System.out.println("[Audience] turn off cell phones.");
	}
	
	public void applaud() {
		System.out.println("[Audience] Wow, Amazing! Applaud.");
	}
	
	public void demandRefund() {
		System.out.println("[Audience] Xu xu xu, yu yu yu~");
	}
}
